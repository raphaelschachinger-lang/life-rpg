export function isLootUnlocked(item, gameState, playerLevel) {
  if (item.type === 'level')      return playerLevel >= item.levelRequired;
  if (item.type === 'badge')      return !!gameState.badges[item.badgeRequired];
  if (item.type === 'patrimoine') return gameState.patrimoine.current >= item.patrimoineRequired;
  return false;
}
